<!-- JavaScript for tab switching -->
<script>
    document.addEventListener('DOMContentLoaded', function () {
        // Initialize AOS when the page loads
        AOS.init();

        // Get all tab buttons
        const tabButtons = document.querySelectorAll('.filter-tab');

        // Add click event to each tab button
        tabButtons.forEach(button => {
            button.addEventListener('click', function () {
                // Remove active class from all tabs
                tabButtons.forEach(btn => btn.classList.remove('active'));

                // Add active class to clicked tab
                this.classList.add('active');

                // Hide all sections
                const sections = document.querySelectorAll('.table-section');
                sections.forEach(section => section.style.display = 'none');

                // Show the target section
                const targetId = this.getAttribute('data-target');
                document.getElementById(targetId).style.display = 'block';

                // Refresh AOS to detect and animate new visible elements
                setTimeout(function () {
                    AOS.refresh();
                }, 10); // Short timeout to ensure the display change has completed
            });
        });

        // Trigger click on first tab to initialize the view
        if (tabButtons.length > 0) {
            tabButtons[0].click();
        }
    });
</script>
