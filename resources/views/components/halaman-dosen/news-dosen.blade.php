    <script>
        document.addEventListener("DOMContentLoaded", function () {
            const searchInput = document.querySelector(".news2-search-input");
            const monthSelect = document.querySelector(".news2-dropdown");
            const searchBtn = document.querySelector(".news2-search-btn");
            const newsItems = document.querySelectorAll(".news2-item");
            const resultsInfo = document.querySelector(".news2-results-info");

            function filterNews() {
                const keyword = searchInput.value.trim().toLowerCase();
                const selectedMonth = monthSelect.value.toLowerCase();
                let visibleCount = 0;

                newsItems.forEach(item => {
                    const title = item.querySelector(".news2-title").innerText.toLowerCase();
                    const excerpt = item.querySelector(".news2-excerpt").innerText.toLowerCase();
                    const dateText = item.querySelector(".news2-date").innerText.toLowerCase();

                    let matchesKeyword = keyword === "" || title.includes(keyword) || excerpt.includes(keyword);
                    let matchesMonth = selectedMonth.includes("-- select all") || dateText.includes(selectedMonth);

                    if (matchesKeyword && matchesMonth) {
                        item.style.display = "";
                        visibleCount++;
                    } else {
                        item.style.display = "none";
                    }
                });

                resultsInfo.textContent = `Results: ${visibleCount} of ${newsItems.length}`;
            }

            searchBtn.addEventListener("click", filterNews);
            searchInput.addEventListener("keypress", function (e) {
                if (e.key === "Enter") {
                    filterNews();
                }
            });
        });
        </script>

